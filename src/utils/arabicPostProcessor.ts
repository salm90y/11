/**
 * الدالة الرئيسية لتنظيف وتصحيح النصوص العربية المفرغة (Arabic Post-Processor)
 * تحافظ على تقسيم الأسطر كما هي وتصحح الأخطاء المطبعية الرسمية والقانونية.
 */
export function normalizeAndCorrectArabicText(text: string): string {
  if (!text) return "";

  // 1. إزالة كشيدة ومد الحروف (ـ)
  let cleaned = text.replace(/ـ+/g, '');

  // 2. تطبيق قواعد التصحيح اللغوي والقانوني
  for (const [pattern, repl] of OFFICIAL_LEGAL_CORRECTIONS) {
    cleaned = cleaned.replace(pattern, repl);
  }

  // 3. تنظيف المسافات الزائدة في نفس السطر مع الحفاظ على أسطر السطر الجديد (\n)
  const lines = cleaned.split('\n');
  const processedLines = lines.map(line => {
    return line.replace(/[ \t]+/g, ' ').trim();
  });

  return processedLines.join('\n').trim();
}

