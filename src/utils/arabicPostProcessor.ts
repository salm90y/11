/**
 * دالة حساب المسافة النصية (Levenshtein Distance) للمطابقة مع القاموس
 */
function levenshteinDistance(a: string, b: string): number {
  const matrix: number[][] = [];
  for (let i = 0; i <= b.length; i++) matrix[i] = [i];
  for (let j = 0; j <= a.length; j++) matrix[0][j] = j;

  for (let i = 1; i <= b.length; i++) {
    for (let j = 1; j <= a.length; j++) {
      if (b.charAt(i - 1) === a.charAt(j - 1)) {
        matrix[i][j] = matrix[i - 1][j - 1];
      } else {
        matrix[i][j] = Math.min(
          matrix[i - 1][j - 1] + 1,
          matrix[i][j - 1] + 1,
          matrix[i - 1][j] + 1
        );
      }
    }
  }
  return matrix[b.length][a.length];
}

/**
 * مطابقة وتصحيح الكلمة الواحدة مع المعجم أوفلاين/أونلاين
 */
export function dictionaryLookupWordCorrection(word: string): string {
  if (!word || word.length < 3) return word;

  // تنظيف الحركات والعلامات
  const cleanW = word.replace(/[^\u0621-\u064A]/g, '');
  if (!cleanW || cleanW.length < 3) return word;

  // 1. فحص الكلمات المؤنثة المنتهية بـ "ه" وتحويلها لـ "ة"
  if (FEMININE_CORRECTIONS[cleanW]) {
    return word.replace(cleanW, FEMININE_CORRECTIONS[cleanW]);
  }

  // 2. إذا كانت الكلمة موجودة بالفعل في المعجم
  if (LOCAL_ARABIC_DICTIONARY.has(cleanW)) {
    return word;
  }

  // 3. تصحيح أخطاء الأحرف المتشابهة الشائعة (د/ذ، ر/ز، ع/غ، ط/ظ)
  let bestMatch = '';
  let minDistance = Infinity;

  for (const dictWord of LOCAL_ARABIC_DICTIONARY) {
    if (Math.abs(dictWord.length - cleanW.length) > 2) continue;

    const dist = levenshteinDistance(cleanW, dictWord);
    // إذا كانت المسافة حرفاً واحداً خطأ بالنسبة لكلمة طويها 4+ أحرف
    if (dist <= 1 && cleanW.length >= 4) {
      if (dist < minDistance) {
        minDistance = dist;
        bestMatch = dictWord;
      }
    } else if (dist <= 2 && cleanW.length >= 7) {
      if (dist < minDistance) {
        minDistance = dist;
        bestMatch = dictWord;
      }
    }
  }

  if (bestMatch) {
    return word.replace(cleanW, bestMatch);
  }

  return word;
}

/**
 * الدالة الرئيسية لتنظيف وتصحيح النصوص العربية المفرغة (Arabic Post-Processor)
 */
export function normalizeAndCorrectArabicText(text: string): string {
  if (!text) return "";

  // 1. إزالة كشيدة ومد الحروف (ـ)
  let cleaned = text.replace(/ـ+/g, '');

  // 2. تطبيق خريطة الجمل والعبارات الرسمية الثابتة
  for (const [phrase, correction] of Object.entries(OFFICIAL_PHRASES_MAP)) {
    cleaned = cleaned.replace(new RegExp(phrase, 'gi'), correction);
  }

  // 3. تصحيح أنماط تشابك الأسطر وتكرار حرف السين الناتج عن وصلات السكانر
  const regexReplacements: [RegExp, string][] = [
    [/المسسوضوع/g, 'الموضوع'],
    [/المسسسوضوع/g, 'الموضوع'],
    [/المسسؤول/g, 'المسؤول'],
    [/المسسؤلية/g, 'المسؤولية'],
    [/مديرسية/g, 'مديرية'],
    [/مديرسسية/g, 'مديرية'],
    [/مديرسيةشرطة/g, 'مديرية شرطة'],
    [/التارسيخ/g, 'التاريخ'],
    [/التارسسخ/g, 'التاريخ'],
    [/التقاعسسد/g, 'التقاعد'],
    [/التقاعسد/g, 'التقاعد'],
    [/وكالسسة/g, 'وكالة'],
    [/وزارسسة/g, 'وزارة'],
    [/شعبيسسة/g, 'شعبة'],
    [/شعبيسة/g, 'شعبة'],
    [/الداخليسسة/g, 'الداخلية'],
    [/الداخليسة/g, 'الداخلية'],

    // الترويسات والرموز الرسمية
    [/العدد\s*[:;\s]*أم/g, 'العدد: '],
    [/التاريخ\s*[:;\s]*/g, 'التاريخ: '],
    [/الموضوع\s*[:;\s]*\/?\s*/g, 'الموضوع / '],
    [/إلى\s*\//g, 'إلى / '],
    [/المرفقات\s*[:;\s]*\/?\s*/g, 'المرفقات / '],

    // إصلاح رموز المسح الضوئي المكررة
    [/[;،,]{2,}/g, ' / '],
    [/\s+/g, ' ']
  ];

  for (const [pattern, repl] of regexReplacements) {
    cleaned = cleaned.replace(pattern, repl);
  }

  // 4. تطبيق Dictionary Lookups لكل كلمة على حدة
  const lines = cleaned.split('\n');
  const processedLines: string[] = [];

  for (const line of lines) {
    const lineStr = line.trim();
    if (!lineStr) continue;

    const words = lineStr.split(' ');
    const correctedWords = words.map(w => dictionaryLookupWordCorrection(w));
    processedLines.push(correctedWords.join(' '));
  }

  return processedLines.join('\n');
}
